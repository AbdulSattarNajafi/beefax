const galleryLinks = document.querySelector('.gallery-links');
const gallery = document.querySelector('.gallery-items');
window.addEventListener('DOMContentLoaded', function() {
  displayGalleryItems(items)
  displayGalleryBtns();
});

function displayGalleryItems(eachItem) {
  let displayItems = eachItem.map(function(item) {
    return `<div class="item">
    <div class="item-img">
      <img src=${item.img} class="responsive-img" alt="image">
    </div>
    <div class="item-desc">
      <h3>${item.title}</h3>
      <h5>${item.category}</h5>
      <p>${item.desc}</p>
      <div class="item-links">
        <button type="button" class="primary-btn">view project</button>
        <button type="button" class="primary-btn">read more</button>
      </div>
    </div>
  </div>`;
  });
  displayItems = displayItems.join("");
  gallery.innerHTML = displayItems;
};

function displayGalleryBtns(){
  const categories = items.reduce(function(values, item) {
    if(!values.includes(item.category)) {
      values.push(item.category);
    }
    return values;
  }, ['all']);
  let categoryBtn = categories.map(function(category) {
    return `<button type="button" class="filter-btn primary-btn" data-id=${category}>${category}</button>`;
  });
  categoryBtn = categoryBtn.join("");
  galleryLinks.innerHTML = categoryBtn;
  //Filtering The items
  const filterBtns = document.querySelectorAll('.filter-btn');
  filterBtns.forEach(function(aBtn) {
    aBtn.addEventListener('click', function(elm) {
      const categories = elm.currentTarget.dataset.id;
      const galleryCategory = items.filter(function(galleryItem) {
        if(galleryItem.category === categories) {
          return galleryItem;
        }
      });
      if(categories === "all") {
        return displayGalleryItems(items);
      }else {
        return displayGalleryItems(galleryCategory);
      }
    })
  })
}